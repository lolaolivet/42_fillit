#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*s1;
	char	s2;

	i = 0;
	s1 = (char *)b;
	s2 = (char)c;
	while (i < len)
	{
		s1[i] = s2;
		i++;
	}
	return (s1);
}
